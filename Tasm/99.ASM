;--Печать текста
;  из переменной Y
;  четыре раза

  .model tiny
;-модель памяти:
;  все в одном
;  сегменте
;  (файл типа COM)

  .code
;-начало сегмента
;        кода

  org 100h
;-коды программы
;  начинаются
;  с адреса 100h

Start:
;-начало текста
;        программы

  mov AX, 3
  int 10h
;-очистка экрана



  mov DX, offset Y
;-адрес текста в DX

  mov CX, 4
;-начальный счетчик
;           цикла
;     (количество
;      повторов)







Metka:
;-метка,
;   с которой
;   начинается
;   тело цикла

  mov AH, 9
  int 21h
;-печать текста
;   с адресом из DX
;   до символа '$'

  dec CX
;-уменьшение
;   счетчика
;   цикла на 1

  jnz Metka
;-если счетчик
;   не обнулился,
;   то повтор цикла


  mov AH, 10h
  int 16h
;-пауза
;   (прерывание 16h
;    функция 10h)

  mov AH, 4Ch
  int 21h
;-стоп



  Y db "ABC"
  db 13,10,'$'
;-переменная:
;   13,10 -перевод
;          курсора
;  '$'-конец текста

  end Start
